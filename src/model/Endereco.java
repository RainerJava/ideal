package model;

/**
 *
 * @author Jorge
 */
public class Endereco {

    private int id;
    private String logradouro;
    private String numero;
    private String complemento;
    private String bairro;
    private int id_municipio;
    private String cep;
    private String referencia;

    public Endereco(int id, String logradouro, String numero, String complemento, String bairro, int id_municipio, String cep, String referencia) {
        this.id = id;
        this.logradouro = logradouro;
        this.numero = numero;
        this.complemento = complemento;
        this.bairro = bairro;
        this.id_municipio = id_municipio;
        this.cep = cep;
        this.referencia = referencia;
    }

    public Endereco(String logradouro, String numero, String complemento, String bairro, int id_municipio, String cep, String referencia) {
        this.logradouro = logradouro;
        this.numero = numero;
        this.complemento = complemento;
        this.bairro = bairro;
        this.id_municipio = id_municipio;
        this.cep = cep;
        this.referencia = referencia;
    }

    public Endereco() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getLogradouro() {
        return logradouro;
    }

    public void setLogradouro(String logradouro) {
        this.logradouro = logradouro;
    }

    public String getNumero() {
        return numero;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }

    public String getComplemento() {
        return complemento;
    }

    public void setComplemento(String complemento) {
        this.complemento = complemento;
    }

    public String getBairro() {
        return bairro;
    }

    public void setBairro(String bairro) {
        this.bairro = bairro;
    }

    public int getId_municipio() {
        return id_municipio;
    }

    public void setId_municipio(int id_municipio) {
        this.id_municipio = id_municipio;
    }

    public String getCep() {
        return cep;
    }

    public void setCep(String cep) {
        this.cep = cep;
    }

    public String getReferencia() {
        return referencia;
    }

    public void setReferencia(String referencia) {
        this.referencia = referencia;
    }

}
